
LF EQU 10
CR EQU 13
DIM EQU 5

.MODEL SMALL
.STACK
.DATA

OP1T DB 'Inserisci il primo numero: ','$'
OP2T DB   "Inserisci il secondo numero: ",'$' 
OP1 DB ?
OP2 DB ?
VET DB DIM DUP(?)

.CODE
.STARTUP

MOV CX,DIM
CICLO:
LEA DX, OP1T
MOV AH, 9
INT 21H
MOV AH, 1
INT 21H
SUB AL, 48
MOV OP1, AL

MOV AH,2
MOV DL, LF
INT 21H
MOV DL, CR
INT 21H

LEA DX, OP2T
MOV AH, 9
INT 21H
MOV AH, 1
INT 21H
SUB AL, 48
MOV OP2, AL

MOV AH,2
MOV DL, LF
INT 21H
MOV DL, CR
INT 21H

MOV AL,OP1
MUL OP2
MOV VET[SI],AL
INC SI
LOOP CICLO

.EXIT
END
